package HomeWork;
//Create an array to store char values and then print those in reverse order
public class HWArray5 {
    public static void main(String[] args) {
        char[] grade={'A','B','C','D','E','F'};
        for(int i=5;i>=0;i--){
            System.out.print(grade[i]+" ");
        }
    }
}
