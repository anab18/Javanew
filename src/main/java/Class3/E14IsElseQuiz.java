package Class3;

public class E14IsElseQuiz {
    public static void main(String[] args) {
        int age=20;
         if(age<1){
             System.out.println("Baby");

         }else if(age<20){
             System.out.println("teenager");

         }else if(age<30){
             System.out.println("Still young");

         }else if(age<50){
             System.out.println("old");
         }
    }
}
