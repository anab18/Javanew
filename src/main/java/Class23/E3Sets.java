package Class23;

import java.util.ArrayList;
import java.util.HashSet;

public class E3Sets {
    public static void main(String[] args) {
        ArrayList<Integer> num = new ArrayList<>();
        num.add(10);
        num.add(20);
        num.add(12);

        System.out.println(num);


        HashSet<Integer> n=new HashSet<>();
        n.add(10);
        n.add(20);
        num.add(12);
        System.out.println(n);

    }
}
